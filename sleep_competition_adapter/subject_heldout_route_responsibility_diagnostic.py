def select_subject_balanced_routes(cells: pd.DataFrame, score_col: str) -> tuple[pd.DataFrame, pd.DataFrame]:
    grid = route_policy_grid(cells, score_col)
    subject_grid, _subject_policy_audit = policy_subject_audit(cells, grid, score_col)
    chosen = choose_subject_balanced_routes(subject_grid)
    return chosen, subject_grid


def run_subject_heldout(train_cells: pd.DataFrame, score_col: str) -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    subjects = sorted(train_cells["subject_id"].astype(str).unique())
    fold_rows: list[dict[str, Any]] = []
    route_rows: list[dict[str, Any]] = []
    heldout_audits: list[pd.DataFrame] = []
    for subject in subjects:
        selector_cells = train_cells[~train_cells["subject_id"].astype(str).eq(subject)].copy()
        heldout_cells = train_cells[train_cells["subject_id"].astype(str).eq(subject)].copy()
        chosen, _subject_grid = select_subject_balanced_routes(selector_cells, score_col)
        heldout_audit = apply_conserved_routes_to_oof(heldout_cells, chosen, score_col)
        heldout_audit["heldout_subject"] = subject
        heldout_audits.append(heldout_audit)
        selected = heldout_audit[heldout_audit["released"]].copy()
        gain = selected["effective_gain"].to_numpy(dtype=np.float64) if len(selected) else np.array([], dtype=np.float64)
        fold_rows.append(
            {
                "heldout_subject": subject,
                "selected_cells": int(len(selected)),
                "gain_sum": float(gain.sum()) if len(gain) else 0.0,
                "mean_gain": float(gain.mean()) if len(gain) else 0.0,
                "positive_gain_rate": float((gain > 0).mean()) if len(gain) else np.nan,
                "accepted_targets": ",".join(chosen.loc[chosen["accepted"].eq(True), "target"].astype(str).tolist()),
                "held_targets": ",".join(chosen.loc[chosen["accepted"].eq(False), "target"].astype(str).tolist()),
            }
        )
        for _, row in chosen.iterrows():
            target = str(row["target"])
            selected_target = selected[selected["target"].eq(target)]
            route_rows.append(
                {
                    "heldout_subject": subject,
                    "target": target,
                    "accepted": bool(row["accepted"]),
                    "policy": str(row["policy"]),
                    "decoder_action": str(row["decoder_action"]),
                    "fraction": float(row["fraction"]),
                    "heldout_selected_cells": int(len(selected_target)),
                    "heldout_gain_sum": float(selected_target["effective_gain"].sum()) if len(selected_target) else 0.0,
                    "heldout_positive_gain_rate": float((selected_target["effective_gain"] > 0).mean()) if len(selected_target) else np.nan,
                    "accept_reason": str(row["accept_reason"]),
                }
            )
    return pd.DataFrame(fold_rows), pd.DataFrame(route_rows), pd.concat(heldout_audits, ignore_index=True)


def summarize_heldout_targets(heldout_audit: pd.DataFrame) -> pd.DataFrame:
    selected = heldout_audit[heldout_audit["released"]].copy()
    if selected.empty:
        return pd.DataFrame(columns=["target", "selected_cells", "gain_sum", "positive_gain_rate", "positive_subjects", "negative_subjects"])
    by_subject_target = (
        selected.groupby(["heldout_subject", "target"], observed=True)["effective_gain"]
        .sum()
        .reset_index()
    )
    target_subjects = (
        by_subject_target.groupby("target", observed=True)
        .agg(
            positive_subjects=("effective_gain", lambda x: int((x > 0).sum())),
            negative_subjects=("effective_gain", lambda x: int((x < 0).sum())),
        )
        .reset_index()
    )
    target_cells = (
        selected.groupby("target", observed=True)
        .agg(
            selected_cells=("effective_gain", "size"),
            gain_sum=("effective_gain", "sum"),
            mean_gain=("effective_gain", "mean"),
            positive_gain_rate=("effective_gain", lambda x: float((x > 0).mean())),
        )
        .reset_index()
    )
    return target_cells.merge(target_subjects, on="target", how="left").sort_values("target")


def stable_routes_from_heldout(full_chosen: pd.DataFrame, route_rows: pd.DataFrame, target_summary_frame: pd.DataFrame) -> pd.DataFrame:
    target_stats = target_summary_frame.set_index("target").to_dict(orient="index")
    route_accept_rate = route_rows.groupby("target", observed=True)["accepted"].mean().to_dict()
    route_rows = route_rows.copy()
    stable_rows = []
    for _, route in full_chosen.iterrows():
        row = route.to_dict()
        target = str(row["target"])
        stats = target_stats.get(target, {})
        accept_rate = float(route_accept_rate.get(target, 0.0))
        gain_sum = float(stats.get("gain_sum", 0.0))
        positive_subjects = int(stats.get("positive_subjects", 0))
        negative_subjects = int(stats.get("negative_subjects", 0))
        positive_rate = float(stats.get("positive_gain_rate", 0.0)) if pd.notna(stats.get("positive_gain_rate", np.nan)) else 0.0
        stable = (
            bool(row.get("accepted", False))
            and accept_rate >= 0.60
            and gain_sum > 0.0
            and positive_subjects >= max(2, negative_subjects + 1)
            and positive_rate >= 0.60
        )
        row["accepted"] = stable
        row["heldout_accept_rate"] = accept_rate
        row["heldout_gain_sum"] = gain_sum
        row["heldout_positive_subjects"] = positive_subjects
        row["heldout_negative_subjects"] = negative_subjects
        row["heldout_positive_gain_rate"] = positive_rate
        row["accept_reason"] = (
            "heldout_stable_subject_route_responsibility"
            if stable
            else "failed_subject_heldout_route_responsibility"
        )
        if not stable:
            row["policy"] = "hold"
            row["decoder_action"] = "none"
            row["fraction"] = 0.0
            row["selected_cells"] = 0
        stable_rows.append(row)
    return pd.DataFrame(stable_rows).sort_values("target")


def route_decision_frequency(route_rows: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for target, part in route_rows.groupby("target", observed=True):
        decision = Counter(
            f"{policy}|{decoder}|{fraction:.2f}"
            for policy, decoder, fraction in zip(part["policy"], part["decoder_action"], part["fraction"])
        )
        top_decision, top_count = decision.most_common(1)[0]
        rows.append(
            {
                "target": target,
                "heldout_accept_rate": float(part["accepted"].mean()),
                "top_decision": top_decision,
                "top_decision_count": int(top_count),
                "heldout_gain_sum": float(part["heldout_gain_sum"].sum()),
                "heldout_positive_subjects": int((part["heldout_gain_sum"] > 0).sum()),
                "heldout_negative_subjects": int((part["heldout_gain_sum"] < 0).sum()),
            }
        )
    return pd.DataFrame(rows).sort_values("target")


