#!/usr/bin/env python3
"""Subject-heldout route responsibility diagnostic for HS-JEPA.

Subject-balanced route conservation was positive, but it still selected target
routes after seeing all subjects.  This diagnostic is stricter:

    for each held-out subject:
      choose target-route policies using all other subjects only
      apply those policies to the held-out subject
      measure realized row-target action gain

The support scores themselves are generated by the public-free HS-JEPA
listener-conditioned action-support pipeline; route selection and evaluation do
not use public LB, prior submission probabilities, or proprietary embeddings.

This is a LeJEPA-style diagnostic adapter.  It is not HS-JEPA core itself.
"""

